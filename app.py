def analyze_ingredients(text):
    """ Finds and analyzes all elements with E-numbers from a given string text. """
    if not text:
        st.warning("⚠️ Текстът на съставките е празен.")
        return

    found_codes = re.findall(r'[EeЕе]\s*\d{3,4}[a-zA-Zа-яА-Я]?', text)
    cleaned_codes = list(set([code.replace(' ', '').upper().replace('Е', 'E') for code in found_codes]))
    
    if not cleaned_codes:
        st.info("🥦 Браво! Не бяха открити опасни Е-номера или специфични добавки в текста на този продукт.")
        return

    st.markdown(f"### 🔍 Открити добавки ({len(cleaned_codes)} бр.):")
    
    def sorting_key(code):
        if code in E_ADDITIVES:
            group = E_ADDITIVES[code]["group"]
            if "🔴" in group: return 0
            if "🟡" in group: return 1
            return 2
        return 3

    cleaned_codes.sort(key=sorting_key)

    for code in cleaned_codes:
        if code in E_ADDITIVES:
            info = E_ADDITIVES[code]
            if "🔴" in info["group"]:
                st.error(f"🛑 **[{code}] {info['name']}**  \n*Категория:* {info['group']}  \n*Рискове:* {info['desc']}")
            elif "🟡" in info["group"]:
                st.warning(f"⚠️ **[{code}] {info['name']}**  \n*Категория:* {info['group']}  \n*Рискове:* {info['desc']}")
            else:
                st.success(f"✅ **[{code}] {info['name']}**  \n*Категория:* {info['group']}  \n*Информация:* {info['desc']}")
        else:
            st.info(f"❓ **[{code}]**  \n*Категория:* Неизвестна (Разрешена в ЕС)  \n*Забележка:* Добавката е легална, но липсва детайлно описание за нея в текущата база на приложението.")

def fetch_product(barcode):
    """ Fetches product info from Open Food Facts API """
    clean_barcode = str(barcode).strip()
    if not clean_barcode.isdigit():
        st.error("❌ Невалиден баркод! Моля, въведете само цифри.")
        return

    url = f"https://openfoodfacts.org{clean_barcode}.json"
    
    with st.spinner("🔄 Търсене в глобалната база данни..."):
        try:
            response = requests.get(url, timeout=7)
            if response.status_code == 200:
                data = response.json()
                if data.get("status") == 1:
                    product = data["product"]
                    p_name = product.get('product_name', 'Неизвестна марка / продукт')
                    st.success(f"🛍️ Намерен продукт: **{p_name}**")
                    
                    ingredients = product.get("ingredients_text", "")
                    if not ingredients:
                        tags = product.get("ingredients_tags", [])
                        if tags:
                            ingredients = " ".join(tags)
                    
                    if ingredients:
                        st.markdown("**Пълен извлечен състав:**")
                        st.caption(ingredients)
                        analyze_ingredients(ingredients)
                    else:
                        st.warning("⚠️ Продуктът съществува в базата, но няма добавен списък със съставки.")
                else:
                    st.error("❌ Продуктът все още не е регистриран в глобалната база данни Open Food Facts.")
            else:
                st.error(f"❌ Грешка при комуникация със сървъра. Статус код: {response.status_code}")
        except requests.exceptions.Timeout:
            st.error("❌ Връзката отне твърде дълго време (Timeout). Опитайте отново.")
        except Exception as e:
            st.error(f"❌ Възникна неочаквана грешка при извличане: {e}")

